     <h4 class="page-title-bar-heading">Rules and Bylaws</h4>
</div>

<div class="container-fluid">
<div class="rules-content">
                    <ol>
						<li><span class="rules-list-head">Name</span><p>The Association shall be a national 
							organization for historical studies on South India 
							and shall be called South Indian History Congress. 
							It shall steer clear of ideologies and ideology-oriented 
							associations for purposes of affiliation and shall maintain 
							its individuality.</p></li>
						<li><span class="rules-list-head">Office</span><p>The Central Office of the said Congress shall be
							 located at School of Historical Studies, Madurai Kamaraj University, Madurai. 
							 The working hours of the office are from 10a.m. to 4 p.m. on all working days.</p></li>
						<li><span class="rules-list-head">Objectives</span>
							<ul class="rules-sub-list">
								<li>to promote historical studies on South India, </li>
								<li>to co-ordinate research on historical trends in South India with sister organizations,
									</li>
								<li>to serve as forum for exchange of views through annual sessions and special conferences,</li>
								<li>to help in the preservation of historical monuments, relics and records,</li>
								<li>to honour historians, who made substantial contributions to scholarship on South India.
									</li>
								<li>to receive and administer funds for the implementation of the above said objectives</li>
							</ul>
						</li>
                        <li>
							<span class="rules-list-head">Membership and Subscription</span>
							<ul class="rules-sub-list">
								<li>As a national forum, the Congress admits as members those who are interested in historical
									 studies on South India,irrespective of any regional consideration.</li>
								<li>Members of the following categories are admitted:
									<ul class="rules-sub1-list">
										<li>Annual Member, paying Rs.300/- as annual subscription.</li>
										<li>Life Member, paying a subscription of Rs.3000/-</li>
										<li>Representative Institution, paying a subscription of Rs.5000/-</li>
										<li>Donor, paying a subscription of Rs.10000/-</li>
										<li>Patron, paying a subscription of Rs.10000/- or more.</li>
									</ul>
								</li>
								<li>Year is reckoned with the calendar year, i.e. January to December.</li>
								<li>Persons seeking membership are to apply in writing or in prescribed form to the Treasurer and
									 send the subscription to him, subject to approval of admission by the Secretary.</li>
							</ul>
						</li>
                        <li>
							<span class="rules-list-head">Privileges of Members</span>
							<ul class="rules-sub-list">
								<li>Members are entitled to attend the academic session of the Congress, 
									present papers and participate in discussions. 
									They are eligible to receive printed copies of the proceedings of the Congress
									 at the annual conference on availability.</li>
								<li>The following members shall be entitled to vote/contest in the elections for the Executive 
									Committee / Consultative Committee, except otherwise provided:
									<ul class="rules-sub1-list">
										<li>Patrons and Donors,</li>
										<li>Representative Institutions,</li>
										<li>Life Members who have attended at least one session (excluding the current session).</li>
									</ul>
								</li>
								<li>
									The list of Life Members, eligible to vote/contest in the election shall be displayed at the Secretary's office on the first day of the annual session.
								</li>
							</ul>
						</li>
						<li>
							<span class="rules-list-head">Academic Session</span>
							<p>The Congress shall meet for its academic session usually in January every year. 
							There shall be a general session presided over by the President or in his absence the senior Vice-President. 
							This will be followed by sectional meetings, organized on thematic basis and each presided over by the Sectional President. 
							The papers that are presented for discussion and accepted by the Sectional President will be considered for publication in the proceedings of the Congress.</p>
						</li>
                        <li>
							<span class="rules-list-head">General Body and Business Session</span>
							<ul class="rules-sub-list">
								<li>The General Body is the Supreme authority on policy decisions. 
										It shall consist of members who are eligible to vote as per Article 5(b). 
										However, members not having the voting right, 
										are eligible to attend the meeting of the General Body and participate in the discussion, 
										unless otherwise decided by the Executive Committee.
								</li>
								<li>The members shall be given notice of 21 days in advance about the meeting of the General Body. 
									This will be held during the annual session of the Congress. 
									One-tenth of the members present shall form the quorum. 
									Resolutions duly seconded may be moved with atleast one day's notice and with the permission of the chair.
								</li>
								<li>
									General Body shall be the competent authority to take decisions on policy matters. 
									It shall elect members of the Executive Committee and Office- bearers and Convener and 
									Members of the Consultative Committee of the Congress. It shall have the authority to 
									remove any member/ any Office-bearer from respective positions, provided it is by a 
									two-thirds majority of the members present.
								</li>
								<li>
									It may approve the annual report and the annual budget and ratify the decisions of the Executive Committee / Consultative Committee.
								</li>
							</ul>
						</li>
                        <li>
							<span class="rules-list-head">Executive Committee</span>
							<ul class="rules-sub-list">
								<li>Executive Committee shall be responsible for implementing the policies, programmes and directives of the General Body.
									It shall consist of a President, three Vice-Presidents, (two elected and one nominated), a Secretary,
									two Joint Secretaries, a Treasurer, an Editor and fifteen members. 
									All the office-bearers and members, except the President and one Vice-President shall be elected by the General Body.
								</li>
								<li>The President and Sectional Presidents shall be nominated by the Executive Committee.</li>
								<li>The out-going President shall be nominated as one of the Vice-Presidents by the Executive Committee.</li>
								<li>The Convener and members of the Consultative Committee shall be Ex-officio members of the Executive Committee.</li>
								<li>The President shall hold office for one year. The two elected Vice-Presidents, General Secretary, Joint Secretaries,
									Treasurer and the Editor and members of Editorial Committee and 15 elected members of the Executive Committee
									shall hold office for two years; but are not eligible for seeking election for the same office beyond two consecutive terms.
									The Editor and members of the Editorial Committee shall be nominated by the Executive Committee.
								</li>
							</ul>
						</li>
                        <li>
							<span class="rules-list-head">Elections</span>
							<p>The elections will be conducted during the annual conference of the Association, 
							when members of the General Body will be present. The Secretary shall appoint the Returning 
							Officer with the approval of the Executive Committee to conduct the election by single, direct and secret vote. 
							The newly elected Executive Committee shall take charge from the day after the election results are announced and 
							shall be in office until the next Executive Committee is elected. Any interim vacancy in the Executive Committee shall be 
							filled in by nomination by the Secretary in consultation with the President/ Convener.</p>
						</li>
                        <li>
							<span class="rules-list-head">Office-bearers and their Responsibilities</span>
							<ul class="rules-sub-list">
								<li><span class="imp">President :</span> The President shall have the overall administrative control of the Congress. He shall give his presidential address at the academic session and preside over the meetings of the General Body and Executive Committee. In case of a tie in the voting process, he shall have the casting vote.</li>
								<li><span class="imp">Vice-Presidents:</span> The Vice-Presidents shall assist the President. One of the Vice-Presidents i.e. the senior among them, shall act for the President in his absence. They shall be in-charge of the enrolment of members in areas assigned by the President and assisted by members of the Executive Committee and report to the General Body.</li>
								<li>
									<span class="imp">General Secretary :</span>
									<ul class="rules-sub1-list">
										<li>The General Secretary shall hold charge of the Central Office. He shall be responsible for the implementation of the decisions of the General Body and the Executive Committee.</li>
										<li>The Secretary shall maintain the records of the Congress and attend the correspondence. It shall be his responsibility to convene meetings of the Congress and record its minutes. He is to report the activities of the Congress to the General Body. Also he is to represent the Congress in legal and official transactions.</li>
									</ul>
								</li>
								<li>
									<span class="imp">Treasurer :</span>
									<ul class="rules-sub1-list">
										<li>Treasurer shall be in charge of the finance of the Congress.</li>
										<li>He shall raise contributions, collect membership fees, issue receipts and maintain accounts.</li>
										<li>He shall deposit the funds in the name of South Indian History Congress in the State Bank of India or any other bank, as decided by the Executive Committee and to be operated jointly by the General Secretary and Treasurer. The Treasurer shall submit audited report and annual budget to the General Body.</li>
										<li>He shall have an imprest amount of not more than Rs.500/- at a time, to be recouped periodically. He shall release funds for purpose authorized by the Executive Committee or in case of the emergency with the approval of the President / Convener and report the same to the Executive Committee.</li>
									</ul>
								</li>
								<li>
									<span class="imp">Joint Secretaries :</span>
									There shall be two Joint Secretaries-one to be elected by the General Body and the other to be nominated by the General Secretary in his local area to assist/ act for him.
								</li>
								<li>
									<span class="imp">Editor :</span>
									The Editor shall be in-charge of publications of the Congress. He is to collect from the Secretary the papers that are presented at the academic sessions, edit, print and publish them in the proceedings in the name of General Secretary as its publisher. The Editor and members of the Editorial Committee are to be nominated by the Executive Committee.
								</li>
							</ul>
						</li>
                        <li>
							<span class="rules-list-head">Auditor</span>
							<p>The General Body shall appoint a qualified auditor at its annual meeting to audit the accounts of the Congress for the year ending 31st December.</p>
						</li>
                        <li>
							<span class="rules-list-head">Local Secretary</span>
							<p>The Local Secretary is the representative of the institution, hosting the annual session. He is to raise contributions and obtain grants from funding agencies. 
								The local secretary is to submit details of audited statement of accounts to the funding agencies and the General Secretary Treasurer within six months after the session is over. 
								The host Institution / Local Secretary is to exempt all office-bearers and members of Executive Committee from the payment of conference delegate fee.</p>
						</li>
                        <li>
							<span class="rules-list-head">Consultative Committee</span>
							<ul class="rules-sub-list">
								<li>There shall be a Consultative Committee. It shall consist of a Convener and two Members elected by the General Body from among senior members of the Congress to hold office for Three years and three ex-officio members - President, Secretary and Treasurer of the Congress.</li>
								<li>In case of any possibility of constitutional breakdown, caused by any act of commission or omission on part of any functionary or otherwise, the Convener is authorized to take necessary steps as warranted by the situation in consultation with the Members of Committee, so as to ensure the smooth working of the Congress. He shall report such instances to the Executive Committee / General Body.</li>
							</ul>
						</li>
                        <li>
							<span class="rules-list-head">Amendment to Bye-Laws</span>
							<p>Any amendment to the rules and bye-laws shall be done by specially convened meeting of the General Body with 21 days prior notice and with the approval of two thirds of the members present.</p>
						</li>
                        <li>
							<span class="rules-list-head">Dissolution</span>
							<p>The Congress shall not be dissolved except by a motion passed by three-fourths majority of members present at a meeting of the General Body, expressly convened for the purpose as per a resolution of the Executive Committee and with three months notice by registered post. The assets of the Congress after meeting liabilities shall be disposed off in accordance with the decision of the dissolution meeting.</p>
						</li>
                    </ol>
